CREATE PROCEDURE ADDPRODUCT @ID AS CHAR(5), @DESC VARCHAR(60), 
                            @PRICE AS INT, @CAT AS VARCHAR(30),
                            @SID AS CHAR(5)
AS 
BEGIN
    INSERT INTO PRODUCT VALUES 
    (
        @ID, @DESC, @PRICE, @CAT, @SID
    );

    -- AUTOMATICALLY DISPLAY WHEN PRODUCT ADDED
    SELECT * FROM PRODUCT
    WHERE PID = @ID;
END;



