CREATE PROCEDURE ADDORDER @ID AS CHAR(5), @DATE AS DATE, @CID AS CHAR(5), 
                          @PID AS CHAR(5), @QTY AS INT
AS
BEGIN
    INSERT INTO ORDERS VALUES (
        @ID, @DATE, @CID, @PID, @QTY
    );

    --AUTOMATICALLY DISPLAY ADDED ORDERS
    SELECT * FROM ORDERS
    WHERE OID = @ID;
END;

